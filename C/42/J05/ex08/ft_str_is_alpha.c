#include "main.h"

int		ft_str_is_alpha(char *str)
{
	while (*str++)
		if (!((*(str - 1) >= 'a' && *(str - 1) <= 'z')
		|| (*(str - 1) >= 'A' && *(str - 1) <= 'Z')))
			return (0);
	return (1);
}
