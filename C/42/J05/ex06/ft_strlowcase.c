#include "main.h"

char	*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (str[i++])
		if (str[i - 1] >= 'A' && str[i - 1] <= 'Z')
			str[i - 1] -= ('A' - 'a');
	return (str);
}
