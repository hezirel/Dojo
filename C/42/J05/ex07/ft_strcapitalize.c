#include "main.h"

char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	while (str[i++])
		if ((i == 1
		|| !((str[i - 2] >= 'a' && str[i - 2] <= 'z')
			|| (str[i - 2] >= 'A' && str[i - 2] <= 'Z')))
		&& (str[i - 1] >= 'a' && str[i - 2] <= 'z'))
			str[i - 1] -= ('a' - 'A');
	return (str);
}
