#include "main.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
		ft_putchar('-');
	if (nb <= -10 || 10 <= nb)
		ft_putnbr((nb < 0) ? -(nb / 10) : (nb / 10));
	ft_putchar((nb > 0) ? ('0' + (nb % 10)) : ('0' + -(nb % 10)));
}
