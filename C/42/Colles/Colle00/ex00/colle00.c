#include "colle.h"

void	colle(int x, int y)
{
	int		i;
	int		j;

	i = 0;
	while (i++ < y && x > 0)
	{
		j = 0;
		while (j++ < x)
			if (i == 1 || i == y)
				ft_putchar((j == 1 || j == x) ? 'o' : '-');
			else
				ft_putchar((j == 1 || j == x) ? '|' : ' ');
		ft_putchar('\n');
	}
}
