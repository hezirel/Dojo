#include "main.h"

int		ft_find_next_prime(int nb)
{
	int i;

	while ((i = (nb - 1)) || 1)
	{
		while (i && (nb = (nb < 0) ? 0 : nb) && nb % i--)
			;
		if (nb++ > 1 && !i)
			return (nb - 1);
	}
}
