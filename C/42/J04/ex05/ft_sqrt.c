#include "main.h"

int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	nb = ((nb < 0) ? -nb : nb);
	while ((i * i) < nb)
		i++;
	return (((i * i) == nb) ? i : 0);
}
